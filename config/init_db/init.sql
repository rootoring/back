<p>При обработке HTML браузер может обнаружить ошибки в коде и если они найдены, пытается максимально правильно отобразить конечный результат. Ошибки могут быть разных типов, такие как: не закрытые теги, отсутствие нужных атрибутов, не правильно подключенный файл. Все это тормозит парсинг HTML.</p>

<h3>Парсинг JavaScript</h3>

<p>C прочтением js кода дело обстоит немного проблемнее. Он так же, как и HTML разметка обрабатывается сверху вниз, последовательно, есть конечно исключения в виде асинхронного Web api , но это уже отдельная тема для обсуждения.</p>

<p>Проблема состоит  в том, что при парсинге JavaScript останавливается парсинг HTML и запускается только после завершения обработки JS. Поэтому не стоит подключать скрипты, которые взаимодействуют в теге <strong>head</strong>, так как код не отработает. Выполняйте подключение в конце, перед закрывающим тегом <strong>body</strong>.</p>

