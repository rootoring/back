         return { post }
    }       
  </xmp></code></pre>
  <div class="copy"><i class="far fa-copy" aria-hidden="true"></i></div>
</div>
 

<p>Их отличие в том что asyncData не воспринимает ключевое слово this, требует обязательный возврат значения и доступно только для страниц. </p>

