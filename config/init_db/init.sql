<h3>Что такое SSR<h3> 

<p>В современных веб приложениях которые генерируются на языке JavaScript существуют два основных способа загрузки контента на страницу, первым из них является СSR (client side rendering) где содержимое страницы формируется на клиентской части приложения. У этого подхода есть большой минус, страницы становятся неподвластны seo продвижению. Программисты придумали выход из этой ситуации, они создали SSR, при загрузке на стороне сервера поисковые роботы видят контент страницы, чего не происходило при CSR.</p>

<h3>Как использовать:</h3> 

<p>В Nuxt разработчки фреймворка дали вам две встроенные функции asyncData и fetch. В этих функциях вы должны подгружать контент на страницу с помощью запросов на сервер. Например:</p>

<div class="code">
  <pre><code><xmp>
    async fetch({store, params}) {
